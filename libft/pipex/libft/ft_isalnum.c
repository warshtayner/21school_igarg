#include "libft.h"

int	ft_isalnum(int asci)
{
	if ((asci > 64 && 91 > asci) || (asci > 96 && 123 > asci)
		|| (asci > 47 && 58 > asci))
	{
		return (1);
	}
	return (0);
}
